    private void adiciona_pagamento() {
        if (altDinheiro.isSelected()) {
            this.pagamento = "Dinheiro";
        }
        if (altDebito.isSelected()) {
            this.pagamento = "Debito";
        }
        if (altCredito.isSelected()) {
            this.pagamento = "Credito";
        }
        if (altCheque.isSelected()) {
            this.pagamento = "Cheque";
        }
        String a;
        String b;
        String c;
        String d;
        String sql = "insert into tbformapagamento (IdVenda, Forma_Pagamento, Dinheiro, Dia) values (?,?,?,?)";
        float precoVenda = Float.parseFloat(this.preco.replaceAll(",", "."));
        this.recebeu += Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", "."));
        faltaReceber = precoVenda - recebeu;
        if (faltaReceber <= 0) {
            troco = -faltaReceber;
            faltaReceber = 0;
            lblRecebido.setForeground(lblPreco.getForeground());
        }
        lblRecebido.setText("R$ " + Float.toString(recebeu));
        lblFaltaReceber.setText("R$ " + Float.toString(faltaReceber));
        lblTroco.setText("R$ " + Float.toString(troco));
        if (pagamento.equalsIgnoreCase("Cheque")) {
            a = Integer.toString(calDia.getCalendar().get(Calendar.DATE));
            b = veMes(calDia.getCalendar().get(Calendar.MONTH));
            c = Integer.toString(calDia.getCalendar().get(Calendar.YEAR));
            if (slctTipoCheque.getSelectedIndex() == 0) {
                try {
                    pst = conexao.prepareStatement(sql);
                    pst.setString(1, this.venda);
                    pst.setString(2, this.pagamento);
                    if (Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) > faltaReceber) {
                        pst.setString(3, Float.toString(Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) - troco));
                    } else {
                        pst.setString(3, txtAdicionaPagamento.getText().replaceAll(",", "."));
                    }
                    d = a + "/" + b + "/" + c;
                    pst.setString(4, d);
                    if (Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) - troco>0)
                        pst.executeUpdate();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            } else if (slctTipoCheque.getSelectedIndex() == 1 || slctTipoCheque.getSelectedIndex() == 2) {
                for (int i = 0; i < slctTipoCheque.getSelectedIndex() + 1; i++) {
                    sql = "insert into tbformapagamento (IdVenda, Forma_Pagamento, Dinheiro, Dia) values (?,?,?,?)";
                    b = veMes(calDia.getCalendar().get(Calendar.MONTH) + i);
                    try {
                        pst = conexao.prepareStatement(sql);
                        pst.setString(1, this.venda);
                        pst.setString(2, this.pagamento);
                        pst.setString(3, Float.toString(Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) / (slctTipoCheque.getSelectedIndex() + 1)));
                        d = a + "/" + b + "/" + c;
                        pst.setString(4, d);
                        pst.executeUpdate();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }

                }

            }

        } else {
            a = Integer.toString(data.get(Calendar.DATE));
            b = veMes(data.get(Calendar.MONTH));
            c = Integer.toString(data.get(Calendar.YEAR));
            try {
                pst = conexao.prepareStatement(sql);
                pst.setString(1, this.venda);
                pst.setString(2, this.pagamento);
                if ((Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) > faltaReceber)) {
                    pst.setString(3, Float.toString(Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) - troco));
                } else {
                    pst.setString(3, txtAdicionaPagamento.getText().replaceAll(",", "."));
                }
                d = a + "/" + b + "/" + c;
                pst.setString(4, d);
                if (Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) - troco>0)
                    pst.executeUpdate();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
        // Atualiza caixa -> sendo cheque
        if (pagamento.equalsIgnoreCase("Cheque")) {
            try {
                float mais;
                if (Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) > faltaReceber) {
                        mais = Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) - troco;
                    } else {
                        mais = Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", "."));
                    }
                this.adicionado_cheque += mais;
                sql = "update tbcaixa set Cheque =" + Float.toString(this.caixa_cheque + this.adicionado_cheque) + " where Id = " + this.unidade;
                pst.executeUpdate(sql);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
            
            if (pagamento.equalsIgnoreCase("Dinheiro")) {
                
                try {
                    float mais;
                    if (Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) > faltaReceber) {
                            mais = Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", ".")) - troco;
                        } else {
                            mais = Float.parseFloat(txtAdicionaPagamento.getText().replaceAll(",", "."));
                        }
                    this.adicionado_dinheiro += mais;
                    sql = "update tbcaixa set Dinheiro =" + Float.toString(this.caixa_dinheiro + this.adicionado_dinheiro) + " where Id = " + this.unidade;
                    pst.executeUpdate(sql);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            
            
        }
        

    }

    // Calendar.MONTH retorna 0 pra JAN, 1 pra FEV, etc. A seguir fazemos retonar 01 p JAN, 02 p FEV, etc
    private String veMes(int mes) {
        String b = "00";
        switch (mes) {
            case 0:
                b = "01";
                break;
            case 1:
                b = "02";
                break;
            case 2:
                b = "03";
                break;
            case 3:
                b = "04";
                break;
            case 4:
                b = "05";
                break;
            case 5:
                b = "06";
                break;
            case 6:
                b = "07";
                break;
            case 7:
                b = "08";
                break;
            case 8:
                b = "09";
                break;
            case 9:
                b = "10";
                break;
            case 10:
                b = "11";
                break;
            case 11:
                b = "12";
                break;
            case 12:
                b = "01";
                break;
            case 13:
                b = "02";
        }
        return b;
    }

    private void setar_tabela() {
        String sql = "select Forma_Pagamento as Pagamento, Dinheiro as Pago, Dia from tbformapagamento WHERE IdVenda = " + this.venda;
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            tblVenda.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
    }

