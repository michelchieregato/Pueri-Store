    private void pesquisar(int unidade){
        String sql = "select Codigo, Descricao, Tamanho, " + qualEstoque(unidade) + "," + qualEstoque(slctUnidade.getSelectedIndex()) +  " from tbprodutos where Descricao like ? and Tamanho like ?";
            
        try {
            pst = conexao.prepareStatement(sql);
            // Passa conteudo da pesquisa para o ?
            // Atenção para o "%" - necessário para fazer a busca dinamica -> não incluido no string
            if (slctTamanho.getSelectedItem().toString().equalsIgnoreCase(" ")){
                pst.setString(1, "%" + txtProduto.getText() + "%");
                pst.setString(2, "%%");
            }
            else{
                pst.setString(1, "%" + txtProduto.getText() + "%");
                pst.setString(2, "%" + slctTamanho.getSelectedItem().toString() + "%");            
            }
            rs=pst.executeQuery();
            // Usaremos a biblioteca p/ preencher a tabela
            tblProdutos.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
