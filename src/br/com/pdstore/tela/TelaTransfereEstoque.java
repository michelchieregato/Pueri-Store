    private String adicionar_estoque(boolean abriu){
        int a;
        String aux = "";
        try {
            if (!abriu) {
                String sql = "insert into tbtransicao_estoque(De, Para, IdVendedor) values (?,?,?)";
                pst = conexao.prepareStatement(sql);
                pst.setString(1, adicionaUnidade(unidade));
                pst.setString(2, (String) slctUnidade.getSelectedItem());
                pst.setString(3, (String) cVendedor.getSelectedItem());
                pst.executeUpdate();
            }
            this.abriuVenda = true;
            // Para pegar o valor de Id da venda.
            pst = conexao.prepareStatement("select IdTrans from tbtransicao_estoque order by IdTrans desc");
            rs = pst.executeQuery("select IdTrans from tbtransicao_estoque order by IdTrans desc");
            // Para pegar o último adicionado
            rs.first();
            aux = rs.getString(1);
            String sql = "insert into tbitens_estoque(IdTrans, Quantidade, Idproduto) values (?,?,?)";
            pst = conexao.prepareStatement(sql);
            pst.setString(1, aux);
            pst.setString(2, txtQnt.getText());
            pst.setString(3, txtCodigo.getText());
            a = pst.executeUpdate();
            
            // Para dar baixa no estoque que transferiu.
            sql = "SELECT " + qualEstoque(this.unidade) + " FROM tbitens_estoque AS Itens JOIN tbprodutos AS Produto ON Itens.Idproduto = Produto.Codigo JOIN tbtransicao_estoque AS Estoque ON Itens.IdTrans = Estoque.IdTrans WHERE Produto.Codigo = "+txtCodigo.getText() +" and Estoque.IdTrans = " + aux;
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            rs.next();
            int estoque = Integer.parseInt(rs.getString(1));
            estoque = estoque - Integer.parseInt(txtQnt.getText());
            sql = "update tbprodutos set " + qualEstoque(this.unidade) + "= " + Integer.toString(estoque) + " where Codigo = " + txtCodigo.getText();
            pst = conexao.prepareStatement(sql);
            pst.executeUpdate();

            // Para dar alta no estoque que recebeu.
            sql = "SELECT " + qualEstoque(slctUnidade.getSelectedIndex()) + " FROM tbitens_estoque AS Itens JOIN tbprodutos AS Produto ON Itens.Idproduto = Produto.Codigo JOIN tbtransicao_estoque AS Estoque ON Itens.IdTrans = Estoque.IdTrans WHERE Produto.Codigo = " + txtCodigo.getText() +" and Estoque.IdTrans = " + aux;
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            rs.next();
            estoque = Integer.parseInt(rs.getString(1));
            estoque = estoque + Integer.parseInt(txtQnt.getText());
            sql = "update tbprodutos set " + qualEstoque(slctUnidade.getSelectedIndex()) + " = " + Integer.toString(estoque) + " where Codigo = " + txtCodigo.getText();
            pst = conexao.prepareStatement(sql);
            pst.executeUpdate();
            // Terminamos as operações necessárias
            
            txtQnt.setText("1");
            slctTamanho.setSelectedIndex(0);
            JOptionPane.showMessageDialog(null, "Até aqui foi");
            preenche_tabela_estoque(this.venda);
            
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        return aux;
    }
    
