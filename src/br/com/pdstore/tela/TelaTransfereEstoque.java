    private void repoe_estoque(){
        // Declaração de variáveis
        String aux = this.venda;
        // Coloca de volta no que transferiu.
        String sql = "SELECT Itens.Quantidade," + qualEstoque(unidade) + ", Produto.Codigo FROM tbitens_estoque AS Itens JOIN tbprodutos AS Produto ON Itens.Idproduto = Produto.Codigo JOIN tbtransicao_estoque AS Estoque ON Itens.IdTrans = Estoque.IdTrans WHERE Produto.Codigo = "+txtCodigo.getText() +" and Estoque.IdTrans = " + aux;
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            rs.first();
            do {                
                int quantidade = rs.getInt(1);
                int estoque = rs.getInt(2); 
                int soma = quantidade + estoque;
                sql = "update tbprodutos set " + qualEstoque(unidade) + " = " + Integer.toString(soma) + " where Codigo = " + rs.getInt(3);
                pst = conexao.prepareStatement(sql);
                pst.executeUpdate();
            } while (rs.next());
        
        // Tira de onde foi transferido. 
        sql = "SELECT Itens.Quantidade," + qualEstoque(slctUnidade.getSelectedIndex()) + ", Produto.Codigo FROM tbitens_estoque AS Itens JOIN tbprodutos AS Produto ON Itens.Idproduto = Produto.Codigo JOIN tbtransicao_estoque AS Estoque ON Itens.IdTrans = Estoque.IdTrans WHERE Produto.Codigo = "+txtCodigo.getText() +" and Estoque.IdTrans = " + aux;
        pst = conexao.prepareStatement(sql);
        rs = pst.executeQuery(sql);
        rs.first();
        do {                
            int quantidade = rs.getInt(1);
            int estoque = rs.getInt(2); 
            int soma = quantidade - estoque;
            sql = "update tbprodutos set " + qualEstoque(slctUnidade.getSelectedIndex()) + " = " + Integer.toString(soma) + " where Codigo = " + rs.getInt(3);
            pst = conexao.prepareStatement(sql);
            pst.executeUpdate();
        } while (rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
            
    }
    
    private String adicionaUnidade(int unidade){
        switch(unidade) {
            case 0:
                return "Verbo Divino";
            case 1:
                return "Aldeia da Serra";
            case 2: 
                return "Aclimação";
            case 3:
                return "Itaim";
            default:
                return "Erro";
        }
    }
    
    private String qualEstoque(int unidade) {
        switch(unidade) {
            case 0:
                return "Estoque_Verbo";
            case 1:
                return "Estoque_Aldeia";
            case 2: 
                return "Estoque_Aclimacao";
            case 3:
                return "Estoque_Itaim";
            default:
                return "Estoque_Verbo";
        }
    }
    
