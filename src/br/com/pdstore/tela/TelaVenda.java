    private void pesquisar(int opcao, int unidade){
        String sql = "";
        if (opcao == 1)
            sql = "select Id, Nome, Telefone, Cpf from tbcliente where Nome like ?";
        else
            switch(unidade){
                // 0 = Verbo, 1 = Aldeia, 2 = Aclimacao, 3 = Itaim - Ver TelaQualUnidade
                case 0:
                    sql = "select Codigo, Descricao, Tamanho, Preco_Venda, Estoque_Verbo from tbprodutos where Descricao like ? and Tamanho like ?";
                    break;
                case 1:
                    sql = "select Codigo, Descricao, Tamanho, Preco_Venda, Estoque_Aldeia from tbprodutos where Descricao like ? and Tamanho like ?";
                    break;
                case 2:
                    sql = "select Codigo, Descricao, Tamanho, Preco_Venda, Estoque_Aclimacao from tbprodutos where Descricao like ? and Tamanho like ?";
                    break;
                case 3:
                    sql = "select Codigo, Descricao, Tamanho, Preco_Venda, Estoque_Itaim from tbprodutos where Descricao like ? and Tamanho like ?";
                    break;
            }
            
        try {
            pst = conexao.prepareStatement(sql);
            // Passa conteudo da pesquisa para o ?
            // Atenção para o "%" - necessário para fazer a busca dinamica -> não incluido no string
            if (opcao == 1){
                pst.setString(1, "%" + txtCliente.getText() + "%");
                rs=pst.executeQuery();
                // Usaremos a biblioteca p/ preencher a tabela
                tblCliente.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else{
                if (slctTamanho.getSelectedItem().toString().equalsIgnoreCase(" ")){
                    pst.setString(1, "%" + txtProduto.getText() + "%");
                    pst.setString(2, "%%");
                }
                else{
                    pst.setString(1, "%" + txtProduto.getText() + "%");
                    pst.setString(2, "%" + slctTamanho.getSelectedItem().toString() + "%");            
                }
                rs=pst.executeQuery();
                // Usaremos a biblioteca p/ preencher a tabela
                tblProdutos.setModel(DbUtils.resultSetToTableModel(rs));
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
